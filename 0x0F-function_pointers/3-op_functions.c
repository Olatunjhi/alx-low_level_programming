#include "3-calc.h"

/**
 * op_add - addition function
 * @a: parameter
 * @b: parameter
 * Return: sum a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - addition function
 * @a: parameter
 * @b: parameter
 * Return: subtract a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - addition function
 * @a: parameter
 * @b: parameter
 * Return: multiply a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - addition function
 * @a: parameter
 * @b: parameter
 * Return: divide a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - addition function
 * @a: parameter
 * @b: parameter
 * Return: modulo a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
